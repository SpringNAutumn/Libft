#include "libft.h"
#include <stdio.h>

// s: La string a enviar.
// fd: El file descriptor sobre el que escribir.

void	ft_putstr_fd(char *s, int fd)
{
	int	i;

	i = 0;
	while (s[i])
	{
		write (fd, &s[i], 1);
		i ++;
	}
}
